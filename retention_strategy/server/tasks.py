def generate_customer_from_profile(
    profile: CustomerProfile, 
    remaining_budget: float,
    queue_position: int,
    total_customers: int,
    task_id: str,
    task_difficulty: str,
    seed: Optional[int] = None
) -> CustomerObservation:
    """
    Generate a CustomerObservation from a profile with randomized values.
    
    Args:
        profile: CustomerProfile defining the customer's characteristics
        remaining_budget: Current remaining budget for the episode
        queue_position: 1-indexed position in the queue
        total_customers: Total number of customers in queue
        task_id: Identifier for the current task
        task_difficulty: Difficulty level of the task
        seed: Optional random seed for reproducibility
        
    Returns:
        CustomerObservation ready for the agent
    """
    if seed is not None:
        random.seed(seed)
    
    clv = random.uniform(*profile.clv_range)
    monthly_rev = random.uniform(*profile.monthly_revenue_range)
    tenure = random.randint(*profile.tenure_range)
    churn_risk = random.uniform(*profile.churn_risk_range)
    sentiment = random.uniform(*profile.sentiment_range)
    
    # Potentially add secondary reasons
    secondary_reasons = []
    all_reasons = list(ChurnReason)
    all_reasons.remove(profile.primary_reason)
    if random.random() > 0.5:
        secondary_reasons.append(random.choice(all_reasons))
    
    return CustomerObservation(
        customer_id=profile.customer_id,
        customer_lifetime_value=round(clv, 2),
        monthly_revenue=round(monthly_rev, 2),
        account_tenure_months=tenure,
        churn_risk_score=round(churn_risk, 3),
        primary_churn_reason=profile.primary_reason,
        secondary_churn_reasons=secondary_reasons,
        current_rate=profile.current_rate,
        competitor_best_rate=profile.competitor_rate,
        current_monthly_fee=profile.monthly_fee,
        sentiment_score=round(sentiment, 2),
        remaining_budget=round(remaining_budget, 2),
        queue_position=queue_position,
        total_customers=total_customers,
        task_id=task_id,
        task_difficulty=task_difficulty,
        done=False,
        reward=0.0,
    )


def get_task(task_id: str) -> TaskConfig:
    """Get the task configuration (only task3_retention_orchestration is available)."""
    if task_id != "task3_retention_orchestration":
        raise ValueError(
            f"Unknown task: {task_id}. Available: ['task3_retention_orchestration']"
        )
    return create_hard_task()


def list_tasks() -> List[str]:
    """List available task IDs."""
    return ["task3_retention_orchestration"]
